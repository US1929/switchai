<h1>Come funziona la bolletta del gas: guida completa</h1>

<p>Capire la <strong>bolletta del gas</strong> non è semplice: tra <strong>voci</strong> tecniche, sigle come PSV, CMEM, RE, RG e aliquote IVA differenziate, il rischio di perdersi è alto. Questa guida spiega ogni componente del <strong>costo gas Smc</strong>, con numeri aggiornati e un esempio pratico su 1.000 Smc.</p>

<p>I dati si basano sulle <strong>regole ARERA v4.0 (febbraio 2026)</strong> e sui valori medi di mercato. Se vuoi confrontare subito le offerte, vai al nostro <a href="/">comparatore online</a>.</p>

<h2 id="materia-prima">1. Materia prima</h2>

<p>La <strong>materia prima</strong> è il costo del gas naturale acquistato sul mercato all'ingrosso. Copre circa il 40–50% della spesa totale ed è composta da due parti:</p>

<ul>
  <li><strong>Quota fissa (PCV/CMC)</strong> — spese di commercializzazione e vendita, in €/anno o €/mese. Tipicamente tra 80 e 150 €/anno.</li>
  <li><strong>Quota variabile (€/Smc)</strong> — il costo del gas effettivamente consumato. Varia in base al tipo di offerta.</li>
</ul>

<h3>Prezzo fisso vs indicizzato</h3>

<p>Le offerte gas si dividono in due grandi categorie:</p>

<ul>
  <li><strong>Prezzo fisso</strong>: il €/Smc è bloccato per 12–24 mesi. Protegge dai rialzi ma non beneficia dei ribassi. Il prezzo è in genere intorno a <strong>0,55–0,75 €/Smc</strong>.</li>
  <li><strong>Prezzo indicizzato</strong>: il €/Smc segue l'andamento del mercato all'ingrosso, tipicamente legato al <strong>PSV (Punto di Scambio Virtuale)</strong> o al <strong>CMEM (CMEM-a)</strong> pubblicato dal GME. Attualmente il PSV è intorno a <strong>0,40–0,50 €/Smc</strong>, più uno spread del venditore (0,05–0,15 €/Smc).</li>
</ul>

