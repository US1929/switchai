<h2 id="faq">Domande frequenti</h2>

<h3>Cos'è il PSV nella bolletta del gas?</h3>
<p>Il <strong>PSV (Punto di Scambio Virtuale)</strong> è il prezzo all'ingrosso del gas in Italia, determinato dal mercato del GME (Gestore Mercati Energetici). Molte offerte indicizzate lo usano come riferimento per calcolare la materia prima.</p>

<h3>Quanto costa 1 Smc di gas?</h3>
<p>Il <strong>costo gas Smc</strong> dipende dal tipo di offerta e dal consumo. A giugno 2026, per un consumo di 1.000 Smc/anno, il costo medio è di circa <strong>1,00–1,20 €/Smc</strong> IVA inclusa, di cui circa 0,40–0,60 €/Smc di sola materia prima.</p>

<h3>Come si calcolano le accise del gas?</h3>
<p>Le <strong>accise</strong> si calcolano moltiplicando il consumo in Smc per l'aliquota fissa di <strong>0,149959 €/Smc</strong>. A differenza della luce, non c'è compensazione oltre soglia per il gas.</p>

<h3>Quali sono le voci della bolletta del gas?</h3>
<p>Le principali <strong>voci della bolletta gas</strong> sono: materia prima (quota fissa + variabile), trasporto e stoccaggio, oneri di sistema (RE, RG, UG2, UG3), accise, addizionale regionale e IVA mista (10% primi 480 Smc, 22% oltre).</p>

<h3>L'addizionale regionale è uguale in tutta Italia?</h3>
<p>No, l'<strong>addizionale regionale</strong> varia da Regione a Regione. ARERA utilizza il valore medio nazionale di <strong>0,0093 €/Smc</strong> per la spesa annua stimata, ma il valore reale dipende dalla tua Regione di residenza.</p>

