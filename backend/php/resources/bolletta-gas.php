<h2 id="confronto">Come confrontare le offerte gas</h2>

<p>Per confrontare correttamente le offerte, tieni d'occhio queste variabili:</p>

<ul>
  <li><strong>€/Smc materia prima</strong> — è la voce più variabile. Fissa vs indicizzata fa la differenza nel tempo.</li>
  <li><strong>Quota fissa annuale (PCV/CMC/CMS)</strong> — può variare da 0 a 150 €/anno. Incide molto sui consumi bassi.</li>
  <li><strong>Spread sul PSV</strong> — nelle indicizzate, spread diversi a parità di PSV danno costi finali molto diversi.</li>
  <li><strong>Sconti e promozioni</strong> — alcuni venditori applicano sconti (β/γ) sul primo anno. Verifica sempre il costo al netto degli sconti.</li>
  <li><strong>IVA e imposte</strong> — sono uguali per tutti i venditori, ma attenzione allo scaglione IVA in base al tuo consumo.</li>
</ul>

